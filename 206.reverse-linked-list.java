class Solution {
    public ListNode reverseList(ListNode head) {
        ListNode prev = null;
        ListNode cursor = head;

        while (cursor != null) {
            head = cursor;
            cursor = cursor.next;
            head.next = prev;
            prev = head;
        }

        return head;
    }
}
